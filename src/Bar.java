
public class Bar {
	private int left, right, height;
	
	public Bar(int left, int right, int height) {
		this.left = left;
		this.right = right;
		this.height = height;
	}

	public int getLeft() {
		return left;
	}

	public int getRight() {
		return right;
	}

	public int getHeight() {
		return height;
	}

}
